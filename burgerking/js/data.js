// 加上 category 反查（給 roast 判斷用）
for (const [cat, items] of Object.entries(MENU)) {
  items.forEach(it => it.cat = cat);
}

function findItem(name) {
  for (const items of Object.values(MENU)) {
    const hit = items.find(i => i.n === name);
    if (hit) return hit;
  }
  return null;
}
